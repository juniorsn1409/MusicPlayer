    ID INT NOT NULL AUTO_INCREMENT,
    NOME VARCHAR(100) NOT NULL,
    ARTISTA VARCHAR(100) NOT NULL,
    ALBUM VARCHAR(100) NOT NULL,
    URI VARCHAR(255) NOT NULL,
    DURACAO INT NOT NULL,
    PRIMARY KEY (ID)
);

